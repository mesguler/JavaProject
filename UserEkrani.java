    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
       
        String yeni_ad = ad_alani.getText();
        String yeni_soyad = soyad_alani.getText();
        String yeni_subeNo = subeNo_alani.getText();
        String yeni_durum = durum_alani.getText();
        String yeni_baslamaTarihi = baslama_alani.getText();
        String yeni_dob = dogum_alani.getText();
        
        int selectedrow = personel_tablosu.getSelectedRow();
        
        if (selectedrow == -1) {
            
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("Çalışanlar Tablosu şu anda boş. ");
            }
            else {
                mesaj_yazisi.setText("Lütfen güncellenecek bir çalışan seçin.");
            }
        }
        else {
            
            int sicilNo = (int)model.getValueAt(selectedrow,0);
            
            
            
            islemler.personelGuncelle(sicilNo,yeni_ad,yeni_soyad,yeni_subeNo,yeni_durum,yeni_baslamaTarihi,yeni_dob);
            
            personelGoruntule();
            
            mesaj_yazisi.setText("Çalışan başarıyla güncellendi...");
        }
        
    }//GEN-LAST:event_guncelleActionPerformed

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
      mesaj_yazisi.setText("");
       
       
       int selectedrow = personel_tablosu.getSelectedRow();
       
       if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               mesaj_yazisi.setText("Çalışan tablosu şu anda boş...");
           }
           else {
               mesaj_yazisi.setText("Lütfen silinecek bir çalışan seçin...");
           }
      
       }
       else {
           int sicilno = (int)model.getValueAt(selectedrow,0);
           
           islemler.personelSil(sicilno);
           
           personelGoruntule();
           
           mesaj_yazisi.setText("Çalışan başarıyla silindi...");
           
       }
    }//GEN-LAST:event_silActionPerformed

