    public void personelGoruntule(){
        model.setRowCount(0);
        
        ArrayList<Personel> personeller = new ArrayList<Personel>();
        personeller = islemler.personelleriGetir();
        
        if (personeller != null){
        
            for (Personel personel : personeller) {
                Object[] eklenecek = {personel.getSicilNo(),personel.getAd(),personel.getSoyad(),personel.getBirim(),personel.getDurum(),personel.getBaslama(),personel.getDob()};
                
                model.addRow(eklenecek);
        
            }
    
        }
    }
