    public void dinamikAra(String ara){
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<DefaultTableModel>(model);
        
        personel_tablosu.setRowSorter(trs);
        
        trs.setRowFilter(RowFilter.regexFilter(ara));
    }
    
